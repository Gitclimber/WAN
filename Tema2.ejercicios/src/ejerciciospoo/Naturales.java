
package ejerciciospoo;


public class Naturales {
    
    
    public static void mais(String[] args) {
        for(int i=0;i<100; i++) {
            System.out.println(i);
        }
    }


public static void menos(String[] args) {
        for(int i=100;i>0; i--) {
            System.out.println(i);
        }
    }
}
